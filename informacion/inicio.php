<a> <span style="color: #ffff">ZOIDS </span> <br> 
<span style="color: #EB298D">(ゾイド Zoido?) </span> <br>
<span style="color: #FFEB45"> es un anime basado en la franquicia de juguetes ZOIDS,
 de TOMY. Fue producido por el estudio XEBEC. Es la primera de un total de cuatro series,
  aunque fue la segunda en ser doblada internacionalmente, luego de Zoids: New Century Zero. 
  Tiene un total de 67 episodios de media hora, aunque la versión occidental se divide en dos partes, 
  Zoids: Chaotic Century de 34 episodios y Zoids: Guardian Force de 33 episodios. 
  Zoids fue dejando la emisión en los países de Latinoamérica el 2 de enero de 2002 así como en canales tales:
   Pawa, Cintv, Conovision, Telesistema, Antenatv, etc.</span><hr>


<h2 align="center">Personajes principales</h2><br>
<h2 align="right">Protagonistas</h2>

<span style="color: #ffff">Van Freiheit </span><br>
<span style="color: #EB298D">(バン・フライハイト, Van Furaihaito?) </span><br>
<span style="color: #FFEB45"> Seiyū: Daisuke Kishio Conocido también como Ban Flyheight por la versión estadounidense, es el protagonista de la serie. 
Al principio es un chico que desea ser un piloto zoid como su padre. En unas ruinas cercanas a su pueblo, 
encuentra a Sieg un organoide plateado, así como a Fine, quien en ese momento dormía en la cápsula contigua a la de Sieg, 
e inicia sus aventuras.
Inicialmente pilota un Shield Liger, pero avanzada la historia este queda dañado irreparablemente por el Geno Saurer de Raven,
hasta que Fine y Sieg lo resucitan, ahora como Blade Liger, más poderoso. Más adelante se une a la Fuerza Guardián,
y se transforma en un piloto experto. Durante la batalla final, trabaja en conjunto con Raven, 
sus amigos y el poderío de ambas naciones, logrando así la victoria.</span><br>


<span style="color: #ffff">Fine Eleceene Lyney </span><br>
<span style="color: #EB298D"> (フィーネ・エリシーヌ・リネ, Fīne Erushīnu Rine?) </span><br>
<span style="color: #FFEB45"> Seiyū: Makiko Ōmoto Es una chica perteneciente a una raza casi extinta en el planeta Zi, 
conocida como Zoidianos. No recuerda su pasado, sin embargo, su primer recuerdo surge cuando lee el pilar de unas antiguas ruinas, el "Zoid Eve",
 a quien luego comenzarán a buscar. Más tarde ella logra recordar su pasado cuando trata de revivir a Shield Liger al ingresar en el interior de Sieg,
pero calla para cuidar de sus compañeros. Dos años después, comienza a trabajar como ayudante del Doctor D, 
un destacado científico. Por accidente vuelve a reunirse con Van,
y junto a él se une a la Fuerza Guardián. Al final de la serie es convocada por Proitzen,
para que, usando sus conocimientos de zoidiana, abra la puerta que conduce al Death Saurer y al Zoid EVE.
Al llegar a este punto tiene en sus manos el poder para detener al Zoid EVE, con ello al Death Saurer,
pero Bang la detiene prometiéndole que detendrá a este zoid para que de este modo la raza humana no corra el mismo destino que la antigua raza zoidiana.
Fine siente mucho afecto por ban incluso lo beso en capítulo 33 de chaotic century.</span><br>


<span style="color: #ffff">Zeke </span><br>
<span style="color: #EB298D"> (ジーク, Shīku?)  </span><br>
<span style="color: #FFEB45">Seiyū: Takuma Suzuki
Conocido también como Zeke por la versión estadounidense, 
es el compañero de batallas de Van. Él es un organoide, 
un sistema desarrollado por los antiguos zoidianos que tiene la capacidad de aumentar la potencia de los zoids con los que se fusiona.</span><br>


<span style="color: #ffff"> Moonbay </span><br>
<span style="color: #EB298D"> (ムンベイ, Munbei?) </span><br>
 <span style="color: #FFEB45">Seiyū: Kumiko Watanabe
Una transportista de cargas, que "contrata" a Van y Fine como guardaespaldas después de que dañen su carga.
 Su zoid es un Gustav. Ella adora el dinero, y es gracias a eso que, junto a sus compañeros, 
 firma un contrato para trabajar dentro del ejército republicano, lo que los termina involucrando en la guerra,
  y luego, conociendo a importantes figuras militares.
Más adelante se convierte en piloto principal para el Ultrasaurus,
 un gigantesco zoid utilizado como arma final de los ejércitos Republicano e Imperial.</span><br>


 <span style="color: #ffff"> Irvine </span> <br> 
 <span style="color: #EB298D"> (アーバイン, Ābainu?) </span><br>
 <span style="color: #FFEB45"> Seiyū: Keiji Fujiwara Es un cazarrecompensas. 
En un principio quiere robar el organoide de Van para utilizarlo en batalla, 
aunque más adelante se alía con este. Su zoid es un Command Wolf. 
Ayuda en varias ocasiones a Ban a derrotar a los enemigos.
 Dos años más tarde, luego de un enfrentamiento con Raven, su zoids es gravemente dañado,
  y su memoria de combate se trasplanta a un nuevo zoid, el Lightning Saix. Además, 
  se alía en varias ocasiones a la Fuerza Guardián, aún sin pertenecer a esta.</span><br>



  <span style="color: #ffff">Thomas Richard Schwarz </span> <br>
  <span style="color: #EB298D"> (トーマ・リヒャルト・シュバルツ, Tōma Rihyaruto Shubarutsu?) </span><br>
  <span style="color: #FFEB45">Seiyū: Kentarō Itō Es un miembro del Ejército Imperial de Guylos, asignado a la Fuerza Guardián. Aparece solo desde la segunda temporada.
 Él es el hermano más joven del Coronel Karl Schwarz, y aunque no es tan reconocido como su hermano en el combate,
  es un experto en computadoras. Su zoids es un Dibison, al que tiene integrado un sistema de computadora llamado Beek,
   que le permite aumentar la eficiencia de su zoid, similar a los organoides. Siente además una atracción por Fine, 
   por lo que compite con Van para ganar su afecto, aunque no es correspondido.</span><br>
<h3 align="left">Antagonistas</h3><br>


<span style="color: #ffff">Raven </span> <br>
<span style="color: #EB298D"> (レイヴン, Reivun?)</span> <br>
<span style="color: #FFEB45">Seiyū: Mitsuki Saiga Es uno de los antagonistas, y rival de Van. Luego de que sus padres fueran asesinados,
 pasa a ser cuidado y entrenado por Proitzen. Es conocido por ser uno de los pilotos zoid más fuertes de su época.
Posee un organoide llamado Shadow. En una ocasión, tras ser derrotado por Van, cae en frustración,
 perdiendo la conciencia. Dos años más tarde, se recupera y vuelve a convertirse en un poderoso enemigo, 
 conduciendo al Geno Breaker. 
 Luego de que su organoide fuera herido de muerte, 
 recapacita y comienza a ayudar a Van en la lucha contra Hiltz.</span> <br>



 <span style="color: #ffff">Gunther Proitzen </span> <br>
 <span style="color: #EB298D"> (ギュンター・プロイツェン, Gyuntā Puroitsen?) </span> <br>
 <span style="color: #FFEB45">Seiyū: Hōchū Ōtsuka
Es el general en jefe, y más tarde, regente del imperio de Guylos. Su objetivo es gobernar utilizando el poder del Death Saurer.
 Es derrotado más tarde por Van y sus aliados.
Luego de dos años más tarde, y haciéndose llamar "Dark Kaizer", 
se asocia con Hiltz y Lise, para planear una nueva lucha que libere al Death Saurer original,
 y así dominar al planeta Zi.</span><br>



 <span style="color: #ffff"> Hiltz </span> <br>
 <span style="color: #EB298D"> (ヒルツ, Hirutsu?) </span><br>
 <span style="color: #FFEB45"> Seiyū: Takahiro Sakurai Un antiguo zoidiano, que busca tomar el planeta Zi y restaurar la civilización zoidiana,
 a través de la destrucción de la humanidad, junto con el poder del Death Saurer. 
 Aparece durante la segunda temporada, siendo el objetivo principal de la Fuerza Guardián. 
 Tiene un organoide rojo llamado Ambient. Su zoids es el Death Stinger, que utiliza para a difundir el miedo en el planeta Zi.
  Más tarde se fusiona con el Death Saurer.</span><br>


<span style="color: #ffff"> Lise </span> <br>
<span style="color: #EB298D"> (リーゼ, Rīze?) </span><br>
<span style="color: #FFEB45"> Seiyū: Noriko Hidaka Es una antigua zoidiana, aliada de Hiltz. 
Usa sus poderes mentales para influir en los humanos y los zoids.
 Su visión oscura de la humanidad se debe a que mientras era niña, 
 su amigo Nico muere tras ser atacado por el ejército republicano.
  Al final de la serie, se aleja de Hiltz, 
  y se une a Raven, por quien además siente una atracción.</span><br>



<span style="color: #ffff">Lista de episodios</span><br>
